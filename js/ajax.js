$(document).ready(function() {    
                $('#btnmos0').on('click', function(){
                    //alert($(this).val());
                    //$('#Info').html('<img src="img/loader.gif" alt="" />').fadeOut(1000);
                    var preg = document.getElementById('pregunta').value;
                    var id = $(this).val();        
                    var dataString = 'z=Update&Id='+id;

                    $.ajax({
                        type: "POST",
                        url: "php/functions.php",
                        data: dataString,
                        success: function(data) {
                            $('#tabla').load('loadadmin.php?load='+preg);
                        }
                    });
                });              
            });

